package name.hersen.android.location;

public class StopPoint {
    public int distance;
    public String name;
    public String site;
    public String area;

    public StopPoint(int distance, String name, String site, String area) {
        this.distance = distance;
        this.name = name;
        this.site = site;
        this.area = area;
    }
}
